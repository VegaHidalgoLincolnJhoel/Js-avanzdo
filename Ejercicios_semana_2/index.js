function sumar(){
    let n1 = parseFloat(document.getElementById("num1").value);
    let n2 = parseFloat(document.getElementById("num2").value);
    let suma = n1 + n2;
    document.getElementById("resultado").textContent = "La suma de los 2 valores es: " + suma;
}

function operaciones(){
    let n1 = parseFloat(document.getElementById("num1").value);
    let n2 = parseFloat(document.getElementById("num2").value);
    let suma = n1 + n2;
    let resta = n1 - n2;
    let multiplicacion = n1 * n2;
    let division = n1 / n2;
    document.getElementById("resultado").textContent = "Las operaciones basicas de los 2 valores son: "+ "\nSuma: " + suma + "\nResta: " + resta + "\nMultiplicacion: " + multiplicacion + "\nDivision: " + division;
}

